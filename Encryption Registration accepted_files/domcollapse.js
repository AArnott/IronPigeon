function newWin(url,widthIgnored,HeightIgnored)
{ var title= "Help";
	var winRef = window.open(url,title,"scrollbars=yes,resizable=yes,status=yes,toolbar=yes,location=yes,width=800,height=600")
	winRef.focus();
}
